async function seedDatabase(forceReseed = false) {
    try {
        const careerCount = await Career.countDocuments();
        if (careerCount === 0 || forceReseed) {
            if (forceReseed && careerCount > 0) {
                await Career.deleteMany({});
                console.log('üóëÔ∏è Cleared existing career data for re-seed');
            }
            await Career.insertMany(careerData);
            console.log(`‚úÖ Seeded ${careerData.length} careers`);
        } else {
            console.log(`‚ÑπÔ∏è Database already has ${careerCount} careers ‚Äî skipping seed`);
        }

        const quizCount = await QuizQuestion.countDocuments();
        if (quizCount === 0 || forceReseed) {
            if (forceReseed && quizCount > 0) {
                await QuizQuestion.deleteMany({});
            }
            await QuizQuestion.insertMany(quizQuestions);
            console.log(`‚úÖ Seeded ${quizQuestions.length} quiz questions`);
        } else {
            console.log(`‚ÑπÔ∏è Database already has ${quizCount} quiz questions ‚Äî skipping seed`);
        }
    } catch (err) {
        console.error('‚ùå Seeding error:', err.message);
    }
}

